/**
 * @file oled_display.c
 * @brief SSD1306 128x32 OLED driver — page-mode rendering with 6x8 font.
 *
 * The SSD1306 is driven via the shared I2C bus in page addressing mode.
 * A 512-byte framebuffer (4 pages × 128 columns) is maintained in SRAM
 * and flushed to the device on each update call.
 *
 * Font: 5x8 pixel glyphs (column-major, LSB at top) with a 1-column spacer.
 * Characters 0x20-0x7E are supported (95 printable ASCII).
 */
#include "oled_display.h"
#include "i2c_bus.h"
#include "ov3660_s3_n16r8.h"
#include "esp_log.h"
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"
#include <string.h>
#include <stdio.h>

static const char *TAG = "oled";

#define OLED_WIDTH   128
#define OLED_PAGES     4    /* 4 pages × 8px = 32px height */
#define OLED_FONT_W    6    /* 5px glyph + 1px spacer */
#define OLED_COLS_PER_LINE (OLED_WIDTH / OLED_FONT_W)  /* 21 chars/line */

static i2c_master_dev_handle_t s_dev = NULL;
static uint8_t s_fb[OLED_PAGES][OLED_WIDTH];   /* Framebuffer */

